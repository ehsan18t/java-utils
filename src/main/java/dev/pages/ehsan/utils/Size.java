package dev.pages.ehsan.utils;

public class Size {
    private double h;
    private double w;

    public Size(double h, double w) {
        this.h = h;
        this.w = w;
    }

    public double getH() {
        return h;
    }

    public void setH(double h) {
        this.h = h;
    }

    public double getW() {
        return w;
    }

    public void setW(double w) {
        this.w = w;
    }
}
